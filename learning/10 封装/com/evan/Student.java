package com.evan;

public class Student {
    private String name;
    private int age;
    private double score;

    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getAge() {
        return this.age;
    }
    
    public void setScore(int age) {
        this.age = age;
    }

    public double getScore(double score) {
        return this.score;
    }
}
